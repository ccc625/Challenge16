public class WeatherData
{
    private String day;
    private String cloudy;

    public WeatherData()
    {

    }

    public void setDay(String value)
    {
        this.day = value;
    }

    public String getDay()
    {
        return this.day;
    }

    public void setCloudy(String value)
    {
        this.cloudy = value;
    }

    public String getCloudy()
    {
        return this.cloudy;
    }


}
